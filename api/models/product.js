const mongoose = require('mongoose');
const db = require('../config/db')

const productSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    category: {
        type: String,
        required: true
    },
    description: {
        type: String,
        required: true
    },
    image: {
        type: String,
        required: true
    },
    price: {
        type: Number,
        required: true
    },
    quantity: {
        type: Number,
        required: true,
        max: 5
    }
});

const addProduct = db.model('product', productSchema)

module.exports = addProduct
